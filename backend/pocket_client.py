import asyncio
import logging
from pocketoptionapi_async import AsyncPocketOptionClient
from config import Config

logger = logging.getLogger("signal_bot")

class PocketOptionClient:
    def __init__(self):
        self.client = None
        self.connected = False
        self._initialized = False  # –î–æ–¥–∞—î–º–æ –ø—Ä–∞–ø–æ—Ä–µ—Ü—å —ñ–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–∞—Ü—ñ—ó
    
    async def initialize(self):
        """–Ü–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–∞—Ü—ñ—è –∫–ª—ñ—î–Ω—Ç–∞ (–≤–∏–∫–ª–∏–∫–∞—î—Ç—å—Å—è –æ–¥–∏–Ω —Ä–∞–∑)"""
        if self._initialized:
            return self
            
        try:
            if not Config.POCKET_SSID:
                logger.error("‚ùå SSID –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ!")
                return self
            
            logger.info(f"üîó –Ü–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–∞—Ü—ñ—è PocketOption –∫–ª—ñ—î–Ω—Ç–∞...")
            
            # –°—Ç–≤–æ—Ä—é—î–º–æ –∫–ª—ñ—î–Ω—Ç
            self.client = AsyncPocketOptionClient(
                ssid=Config.POCKET_SSID,
                is_demo=Config.POCKET_DEMO,
                enable_logging=True
            )
            
            self._initialized = True
            logger.info("‚úÖ –ö–ª—ñ—î–Ω—Ç —ñ–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–æ–≤–∞–Ω–æ")
            return self
            
        except Exception as e:
            logger.error(f"‚ùå –ü–æ–º–∏–ª–∫–∞ —ñ–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–∞—Ü—ñ—ó: {e}")
            return self
    
    async def connect(self):
        """–ü—ñ–¥–∫–ª—é—á–µ–Ω–Ω—è –¥–æ Pocket Option"""
        try:
            # –Ü–Ω—ñ—Ü—ñ–∞–ª—ñ–∑—É—î–º–æ, —è–∫—â–æ —â–µ –Ω–µ –±—É–ª–æ
            if not self._initialized:
                await self.initialize()
            
            if not self.client:
                logger.error("‚ùå –ö–ª—ñ—î–Ω—Ç –Ω–µ —ñ–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–æ–≤–∞–Ω–∏–π")
                return False
            
            logger.info(f"üîó –ü—ñ–¥–∫–ª—é—á–µ–Ω–Ω—è...")
            
            # –ü—ñ–¥–∫–ª—é—á–∞—î–º–æ—Å—è
            connection_result = await self.client.connect()
            
            if connection_result:
                logger.info("‚úÖ –£—Å–ø—ñ—à–Ω–æ –ø—ñ–¥–∫–ª—é—á–µ–Ω–æ –¥–æ PocketOption!")
                self.connected = True
                
                # –¢–µ—Å—Ç—É—î–º–æ –∑'—î–¥–Ω–∞–Ω–Ω—è
                try:
                    balance = await self.client.get_balance()
                    logger.info(f"üí∞ –ë–∞–ª–∞–Ω—Å: {balance.balance} {balance.currency}")
                except Exception as e:
                    logger.warning(f"–ë–∞–ª–∞–Ω—Å –Ω–µ –æ—Ç—Ä–∏–º–∞–Ω–æ: {e}")
                
                return True
            else:
                logger.error("‚ùå –ù–µ –≤–¥–∞–ª–æ—Å—è –ø—ñ–¥–∫–ª—é—á–∏—Ç–∏—Å—è –¥–æ PocketOption")
                self.connected = False
                return False
                
        except Exception as e:
            logger.error(f"‚ùå –ü–æ–º–∏–ª–∫–∞ –ø—ñ–¥–∫–ª—é—á–µ–Ω–Ω—è: {e}")
            self.connected = False
            return False
    
    async def get_candles(self, asset, timeframe, count=50):
        """–û—Ç—Ä–∏–º–∞–Ω–Ω—è —Å–≤—ñ—á–æ–∫"""
        try:
            # –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ —ñ–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–∞—Ü—ñ—é
            if not self._initialized:
                await self.initialize()
            
            # –Ø–∫—â–æ –Ω–µ –ø—ñ–¥–∫–ª—é—á–µ–Ω—ñ, –ø—ñ–¥–∫–ª—é—á–∞—î–º–æ—Å—è
            if not self.connected:
                logger.warning(f"–°–ø—Ä–æ–±—É—é –ø—ñ–¥–∫–ª—é—á–∏—Ç–∏—Å—è –¥–ª—è {asset}...")
                if not await self.connect():
                    logger.error(f"–ù–µ –≤–¥–∞–ª–æ—Å—è –ø—ñ–¥–∫–ª—é—á–∏—Ç–∏—Å—è –¥–ª—è {asset}")
                    return None
            
            logger.info(f"üìä –ó–∞–ø–∏—Ç —Å–≤—ñ—á–æ–∫: {asset}")
            
            # –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ –ø—Ä–∞–≤–∏–ª—å–Ω–∏–π –º–µ—Ç–æ–¥ –∫–ª—ñ—î–Ω—Ç–∞
            candles = await self.client.get_candles(
                asset=asset,
                timeframe=timeframe,
                count=count
            )
            
            if candles and len(candles) > 0:
                logger.info(f"‚úÖ –û—Ç—Ä–∏–º–∞–Ω–æ {len(candles)} —Å–≤—ñ—á–æ–∫ –¥–ª—è {asset}")
                return candles
            else:
                logger.warning(f"–û—Ç—Ä–∏–º–∞–Ω–æ 0 —Å–≤—ñ—á–æ–∫ –¥–ª—è {asset}")
                return None
                
        except Exception as e:
            logger.error(f"‚ùå –ü–æ–º–∏–ª–∫–∞ –æ—Ç—Ä–∏–º–∞–Ω–Ω—è —Å–≤—ñ—á–æ–∫ –¥–ª—è {asset}: {e}")
            return None
    
    async def disconnect(self):
        """–í—ñ–¥–∫–ª—é—á–µ–Ω–Ω—è"""
        try:
            if self.client and self.connected:
                await self.client.disconnect()
                self.connected = False
                logger.info("‚úÖ –í—ñ–¥–∫–ª—é—á–µ–Ω–æ –≤—ñ–¥ PocketOption")
                return True
            return False
        except Exception as e:
            logger.warning(f"–ü–æ–º–∏–ª–∫–∞ –≤—ñ–¥–∫–ª—é—á–µ–Ω–Ω—è: {e}")
            return False
